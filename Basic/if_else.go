package main

import "fmt"

func main() {
	for i := 0; i < 5; i++ {
		if i%2 == 0 {
			fmt.Printf("Even No: %v",i)
			fmt.Println()
		}else{ 
			fmt.Printf("Odd No: %v",i)
			fmt.Println()
		}
	}
	if j:=4; j%2==0 {
		fmt.Printf("Even No: %v",j)
		fmt.Println()
	}else{ 
		fmt.Printf("Odd No: %v",j)
		fmt.Println()
	}
	k:=4;
	if k<9 && k>0 {
		fmt.Println("Hello")
	}
		
}
